    private void jB_cadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jB_cadastrarActionPerformed
        // TODO add your handling code here:
        //String cargo, String turno, String local, String nome, String cpf, double salario
        Secretario novoSecretario = new Secretario(jTF_cargo.getText(), 
                jTF_turno.getText(), 
                jTF_local.getText(),
                jTF_nome.getText(), 
                jTF_CPF.getText(), Double.parseDouble(jTF_salario.getText()));
        dataBase.AdicionaDataBase(novoSecretario);
        
                ArrayList<Funcionario> dados = dataBase.getDataBase();
        String[] nomesColunas={"Nome","Salario","CPF","cargo","turno","local","bonifica√ß√£o"};

        String[][] valoresLinhas = new String[dados.size()][7];

        for(int i = 0; i<dados.size();i++){
            valoresLinhas[i][0] = dados.get(i).getNome();
            valoresLinhas[i][1] = String.valueOf(dados.get(i).getSalario());
            valoresLinhas[i][2] = dados.get(i).getCpf();
            valoresLinhas[i][3] = dados.get(i).getCargo();
            valoresLinhas[i][4] = dados.get(i).getTurno();
            valoresLinhas[i][5] = dados.get(i).getLocal();
            valoresLinhas[i][6] = String.valueOf(dados.get(i).getBonificacao());
        }
        DefaultTableModel tabelaModelo = new DefaultTableModel(valoresLinhas,nomesColunas);

        this.jTable1.setModel(tabelaModelo);
    }//GEN-LAST:event_jB_cadastrarActionPerformed

